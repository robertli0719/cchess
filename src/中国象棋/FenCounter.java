    private static int countFen(int x,int y,int zi)
    {
        switch(zi)
        {
            case ZI.兵:
                return arr兵[x][y];
            case ZI.相:
                return arr相[x][y];
            case ZI.車:
                return arr车[x][y];
            case ZI.士:
                return arr兵[x][y];            
            case ZI.炮:
                return arr炮[x][y];            
            case ZI.马:
                return arr马[x][y];
            case ZI.将:
                return arr将[x][y];
        }
        return 0;
    }
    public static int getFen(int x,int y,int zi)
    {
        if(zi>0)
            return countFen(x,y,zi);
        else if(zi<0)
            return countFen(x,9-y,-zi);
        return 0;
    }

